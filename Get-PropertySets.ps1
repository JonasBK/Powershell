Import-Module ActiveDirectory

$ADRootDSE = Get-ADRootDSE
$ConfigurationNC = $ADRootDSE.configurationNamingContext
$PropertySets = Get-ADObject -Filter {validAccesses -eq 48} -SearchBase "CN=Extended-Rights,$ConfigurationNC" -Properties rightsGuid

foreach ($PropertySet in $PropertySets) {
    Write-Host "######"
    Write-Host "Propery Set: $($PropertySet.Name)"
    Write-Host "Members:"


    # Convert the GUID to byte array
    $GuidBytes = [System.Guid]::Parse($PropertySet.rightsGuid).ToByteArray()

    # Search AD schema for attributes with the specified attributeSecurityGUID
    $Attributes = Get-ADObject -SearchBase (Get-ADRootDSE).schemaNamingContext -Filter { objectClass -eq "attributeSchema" -and attributeSecurityGUID -eq $GuidBytes } -Properties lDAPDisplayName

    foreach ($Attribute in $Attributes) {
        Write-Output "   $($Attribute.Name) ($($Attribute.lDAPDisplayName))"
     }
}

